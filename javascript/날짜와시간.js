const date = new Date();
console.log(date);

console.log(date.getFullYear()); // 현재 연도 출력
console.log(date.getMonth()); // 현재 월 출력
console.log(date.getDate()); // 현재 날짜 출력
console.log(date.getDay()); // 현재 요일 출력
console.log(date.getHours()); // 현재 시간 출력
console.log(date.getMinutes()); // 현재 분 출력
console.log(date.getSeconds()); // 현재 초 출력
console.log(date.getTime()); // 1970년 1월 1일 자정부터 경과한 시간 출력